// Operador artimetico
// Usando valores literais

console.log(10 + 5); // Adição
console.log(10 - 5); // Subtração      
console.log(10 * 5); // Multiplicação
console.log(10 / 5); // Divisão
console.log(10 % 5); // Módulo
console.log(10 ** 5); // Exponenciação

// Usando variáveis
let x = 10; 
let y = 5;

console.log(x + y); // Adição
console.log(x - y); // Subtração        
console.log(x * y); // Multiplicação
console.log(x / y); // Divisão
console.log(x % y); // Módulo
console.log(x ** y); // Exponenciação

// Incremento e Decremento

let z = 10;
(z++)*(y--);

console.log(z++); // Incremento pós-fixado z = z + 1
console.log(z);   // Valor após incremento
console.log(++z); // Incremento pré-fixado 

console.log(z--); // Decremento pós-fixado
console.log(--z);
 

let a = 10;
a += 5; // a = a + 5
console.log(a); // resultado: 15

